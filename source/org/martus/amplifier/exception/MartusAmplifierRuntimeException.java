package org.martus.amplifier.exception;

public class MartusAmplifierRuntimeException extends RuntimeException
{
	public MartusAmplifierRuntimeException()
	{
		super();
	}

	public MartusAmplifierRuntimeException(String arg0)
	{
		super(arg0);
	}

	public MartusAmplifierRuntimeException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

	public MartusAmplifierRuntimeException(Throwable arg0)
	{
		super(arg0);
	}
}
