package org.martus.amplifier.exception;

public class MartusAmplifierApplicationException extends Exception
{
	public MartusAmplifierApplicationException()
	{
		super();
	}

	public MartusAmplifierApplicationException(String arg0)
	{
		super(arg0);
	}

	public MartusAmplifierApplicationException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

	public MartusAmplifierApplicationException(Throwable arg0)
	{
		super(arg0);
	}
}
